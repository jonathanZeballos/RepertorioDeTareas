    private void llenarComboBoxConArchivosTxt(String carpeta) {
        File directorio = new File(carpeta);
        
        // Verificar si la ruta es un directorio válido
        if (directorio.isDirectory()) {
            // Obtener todos los archivos .txt del directorio
            File[] archivosTxt = directorio.listFiles((dir, name) -> name.toLowerCase().endsWith(".txt"));

            // Verificar si hay archivos y llenar el ComboBox
            if (archivosTxt != null && archivosTxt.length > 0) {
                DefaultComboBoxModel<String> modeloComboBox = new DefaultComboBoxModel<>();

                for (File archivo : archivosTxt) {
                    modeloComboBox.addElement(archivo.getName()); // Agregar solo el nombre del archivo
                }

                cbTemas.setModel(modeloComboBox); // Asignar el modelo al ComboBox
            } else {
                cbTemas.addItem("No hay archivos .txt");
            }
        } else {
            cbTemas.addItem("Directorio no válido");
        }
    }
