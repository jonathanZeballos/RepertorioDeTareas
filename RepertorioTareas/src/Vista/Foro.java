    private void llenarTextAreaConArchivo(String rutaArchivo) {
        try (BufferedReader br = new BufferedReader(new FileReader(rutaArchivo))) {
            String linea;
            StringBuilder contenido = new StringBuilder();
            
            // Leer línea por línea el contenido del archivo
            while ((linea = br.readLine()) != null) {
                contenido.append(linea).append("\n"); // Agregar la línea con un salto de línea
            }

            // Establecer el contenido leído en el TxtArea
            txtArea.setText(contenido.toString());

        } catch (IOException e) {
            e.printStackTrace();
        }
    }
    private static void escribirEnArchivo(String rutaArchivo, String contenido) {
        try (BufferedWriter bw = new BufferedWriter(new FileWriter(rutaArchivo, true))) {
            // Escribir contenido en el archivo
            bw.write(user+": "+contenido);
            bw.newLine(); // Escribir un salto de línea
            System.out.println("Escritura en el archivo exitosa.");
        } catch (IOException e) {
            e.printStackTrace();
            System.out.println("Error al escribir en el archivo: " + e.getMessage());
        }
        
    }
